#モジュールの基礎

#--------------------------------------------------
#1.モジュール全体をインポートする
#--------------------------------------------------

#数学の計算ができるmathモジュールを使う
import math #mathモジュールを読み込む

#mathモジュールの中にある「sqrt」(平方根を計算する関数)を使う
数値 = 25
平方根 = math.sqrt(数値) #「モジュール名.関数名」の形で使う
print(f"{数値}の平方根は{平方根}です")

#mathモジュールの中にある「pi」(円周率の定数)を使う
print(f"円周率は{math.pi}です") #「モジュール名.定数名」の形で使う

#--------------------------------------------------
#2.特定の機能だけをインポートする(from...import...)
#--------------------------------------------------

#randomモジュールの中から「randint」関数だけインポートする
from random import randint #★これで「random.randint」と書かずに「randint」だけで使えるようになる

ランダムな数字 = randint(1,100) #1から100までのランダムな整数を生成する
print(f"ランダムな数字:{ランダムな数字}")

#複数の関数をインポートすることもできる
from random import choice,shuffle

#リストの中からランダムに1つ選ぶ
フルーツリスト = ["りんご","梨","オレンジ","桃","さくらんぼ"]
選ばれたフルーツ = choice(フルーツリスト)
print(f"選ばれたフルーツ:{選ばれたフルーツ}")

#リストの要素をシャッフルする(並べ替える)
print(f"シャッフル前:{フルーツリスト}")
shuffle(フルーツリスト) #リスト自体が直接変更される
print(f"シャッフル後:{フルーツリスト}")

#--------------------------------------------------
#3.モジュールに別名をつける(import...as...)
#--------------------------------------------------

import math as m #mathモジュールに「m」という別名をつける

平方根_別名 = m.sqrt(49)
print(f"別名を使った平方根:{平方根_別名}")
print(f"別名を使った円周率:{m.pi}")
