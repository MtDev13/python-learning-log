#リストの作成と表示

print("---複数のデータをまとめて「箱」に入れる---")

#1.いろいろな「おかず」を一つにまとめたリストを作る
#角括弧[]の中に、複数のデータをコンマ(,)で区切って並べる
お弁当のおかずリスト = ["ハンバーグ","マッシュポテト","ブロッコリー","ミニトマト"]
print("今日のおかずリスト")
print(お弁当のおかずリスト)

#2.数字(整数)をまとめたリストを作る
#お弁当の個数を日ごとに記録できる
日ごとの個数リスト = [3,5,2,4]
print("\n日ごとの販売個数リスト")
print(日ごとの個数リスト)

#3.何も入ってないリストも作れる
空っぽのリスト = []
print("\n空っぽのリスト")
print(空っぽのリスト)
